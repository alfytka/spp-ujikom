     */
    public function index()
    {
        $search = User::orderBy('created_at')->latest();
        if (request('search')) {
            $search->where('name', 'like', '%' . request('search') . '%');
        }

        return view('admin.datasiswa.index-siswa', [
            'datasiswa' => $search->paginate(10),
            'datakelas' => Kelas::all()
        ]);
    }

